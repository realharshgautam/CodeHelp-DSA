//GIVEN NO. IS EVEN OR ODD


#include<iostream>
 using namespace std;
 
  int main()
{
  int n;
  cout<<"Check the given no. is even or odd"<<endl;
  cin>>n;

  if (n%2==0)
    {
    cout<<"Given no. is even"<<endl;
    }
  else
    {
    cout<<"Given no. is odd"<<endl;
    }


  return 0;

}